#' Berlin districts (WGS 84)
#'
#' A shape file containing the shape of Berlin.
#'
#' @source \url{https://daten.odis-berlin.de/en/dataset/bezirksgrenzen}
"sf_berlin"

#' Berlin green spaces (WGS 84)
#'
#' A shape file containing the shape of all green spaces (defined as natural
#' areas and landuse categories "forest", "grass", "meadow", "nature_reserve",
#' "scrub", "heath", "beach", and "cliff") in Berlin.
#'
#' @source \url{https://download.geofabrik.de/europe/germany/berlin.html}
"sf_green"

#' Berlin waterways (WGS 84)
#'
#' A shape file containing the shape of all waterways found in Berlin.
#'
#' @source \url{https://download.geofabrik.de/europe/germany/berlin.html}
"sf_water"

#' Berlin roads (WGS 84)
#'
#' A shape file containing all roads in Berlin. Contains only those classes that
#' are used by cars (i.e. excludes roads of class footway, steps, bridleway,
#' path, pedestrian, and cycleway)
#'
#' @source \url{https://download.geofabrik.de/europe/germany/berlin.html}
"sf_roads"

#' Berlin metro station (WGS 84)
#'
#' A shape file containing the locations of metro stations in Berlin.
#'
#' @source \url{https://download.geofabrik.de/europe/germany/berlin.html}
"sf_metro"
